binary_code = input("Введите закодированный текст для декодирования:")

# Убираем пробелы и переносы строк
binary_code = binary_code.replace(" ", "").replace("\n", "")

# Разделяем бинарный код на байты (по 8 бит)
bytes_list = [binary_code[i:i+8] for i in range(0, len(binary_code), 8)]

# Преобразуем бинарные байты в символы
text = "".join([chr(int(byte, 2)) for byte in bytes_list])

# Декодируем из Base64
decoded_text = base64.b64decode(text).decode('utf-8')

print("Декодированный текст:", decoded_text)